public class TeacherBean {
	String te_id;
	String te_name;
	
	String te_depart;
	String te_info;
	String te_email;
	
	
	public String getTe_email() {
		return te_email;
	}
	public void setTe_email(String te_email) {
		this.te_email = te_email;
	}
	public String getTe_id() {
		return te_id;
	}
	public void setTe_id(String te_id) {
		this.te_id = te_id;
	}
	public String getTe_name() {
		return te_name;
	}
	public void setTe_name(String te_name) {
		this.te_name = te_name;
	}
	public String getTe_depart() {
		return te_depart;
	}
	public void setTe_depart(String te_depart) {
		this.te_depart = te_depart;
	}
	public String getTe_info() {
		return te_info;
	}
	public void setTe_info(String te_info) {
		this.te_info = te_info;
	}

}
